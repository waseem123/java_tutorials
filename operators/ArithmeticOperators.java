class ArithmeticOperators {
	public static void main(String[] args) {
		int fnum = 103;
		int snum = 5;
		System.out.println(fnum+snum);
		System.out.println("_______________");

		System.out.println(fnum-snum);
		System.out.println("_______________");

		System.out.println(fnum*snum);
		System.out.println("_______________");

		System.out.println(fnum/snum);
		System.out.println("_______________");

		System.out.println(fnum%snum);
		System.out.println("_______________");
	}
}