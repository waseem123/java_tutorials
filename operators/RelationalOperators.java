class RelationalOperators {
	public static void main(String[] args) {
		int x = 100;
		int y = 150;

		System.out.println(x>y);
		System.out.println(y>x);
		System.out.println("_________");
		System.out.println(x<y);
		System.out.println(y<x);
		System.out.println("_________");
		System.out.println(x>=y);
		System.out.println(y>=x);
		System.out.println("_________");		
		System.out.println(x<=y);
		System.out.println(y<=x);
		System.out.println("_________");		
		System.out.println(x==y);
		System.out.println(y!=x);
		System.out.println("_________");
	}
}